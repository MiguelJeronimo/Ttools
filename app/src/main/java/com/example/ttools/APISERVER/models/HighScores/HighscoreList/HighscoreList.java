public class HighscoreList {
    int rank, level, value;
    String name, vocation, world, title;

    public String getTitle() {
        return title;
    }

    public String getWorld() {
        return world;
    }

    public String getVocation() {
        return vocation;
    }

    public String getName() {
        return name;
    }

    public int getRank() {
        return rank;
    }

    public int getLevel() {
        return level;
    }

    public int getValue() {
        return value;
    }
}
