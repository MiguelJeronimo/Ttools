package com.example.TibiaTools.recyclerview;

public class ItemsRecyclerViewCriatures {
    String lbName, lbrace, imagerace;

    public ItemsRecyclerViewCriatures(String lbName, String lbrace, String imagerace) {
        this.lbName = lbName;
        this.lbrace = lbrace;
        this.imagerace = imagerace;
    }

    public String getLbName() {
        return lbName;
    }

    public void setLbName(String lbName) {
        this.lbName = lbName;
    }

    public String getLbrace() {
        return lbrace;
    }

    public void setLbrace(String lbrace) {
        this.lbrace = lbrace;
    }

    public String getImagerace() {
        return imagerace;
    }

    public void setImagerace(String imagerace) {
        this.imagerace = imagerace;
    }
}
